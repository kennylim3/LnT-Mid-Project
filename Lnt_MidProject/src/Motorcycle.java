public class Motorcycle extends Vehicle{
	
	public Motorcycle(String type, String brand, String name, String license, int topSpeed, int gasCap, int wheel, String tipe, int helm) {
		super();
		this.type = type;
		this.brand = brand;
		this.name = name;
		this.license = license;
		this.topSpeed = topSpeed;
		this.gasCap = gasCap;
		this.wheel = wheel;
		this.tipe = tipe;
		this.helm = helm;
	}
}
